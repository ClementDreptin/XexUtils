        Http::Client client;
        client.AddRsaTrustAnchor(RSA_DN, sizeof(RSA_DN), RSA_N, sizeof(RSA_N), RSA_E, sizeof(RSA_E));

        auto response = client.Get("https://httpbin.org/get?param=value");

